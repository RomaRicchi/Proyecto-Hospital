export const createMovimientoHabitacion = async (req, res) => {
	try {
		const {
			id_admision,
			id_habitacion,
			id_cama,
			fecha_hora_ingreso,
			fecha_hora_egreso,
			id_mov,
			estado,
		} = req.body;

		// üïí Detectar si es reserva (por tipo o fecha futura)
		const fechaIngreso = new Date(fecha_hora_ingreso);
		const esReserva = id_mov === 3 || fechaIngreso > new Date();

		// üîç Buscar admisi√≥n y paciente
		const admisionNueva = await Admision.findByPk(id_admision, {
			include: [{ model: Paciente, as: 'paciente' }],
		});
		if (!admisionNueva)
			return res.status(404).json({ message: 'Admisi√≥n no encontrada' });

		// üîí Validar g√©nero de otro paciente en misma habitaci√≥n
		const movimientoOcupado = await MovimientoHabitacion.findOne({
			where: {
				id_habitacion,
				id_cama: { [Op.ne]: id_cama },
				estado: 1,
				id_mov: 1,
				fecha_hora_egreso: null,
			},
			include: [{
				model: Admision,
				as: 'admision',
				include: [{ model: Paciente, as: 'paciente' }],
			}],
		});

		if (
			movimientoOcupado &&
			movimientoOcupado.admision?.paciente?.id_genero !== admisionNueva.paciente.id_genero
		) {
			return res.status(400).json({
				message: 'No se puede internar porque hay un paciente de diferente g√©nero en la otra cama.',
			});
		}

		// ‚úÖ Crear movimiento habitaci√≥n
		const nuevo = await MovimientoHabitacion.create({
			id_admision,
			id_habitacion,
			id_cama,
			fecha_hora_ingreso,
			fecha_hora_egreso: fecha_hora_egreso || null,
			id_mov,
			estado: estado || 1,
		});

		// üõèÔ∏è Marcar cama como ocupada en cualquier caso (ingreso o reserva)
		if (id_cama) {
			const cama = await Cama.findByPk(id_cama);
			if (cama) {
				cama.estado = 1; // Ocupada
				await cama.save();
			}
		}

		res.status(201).json(nuevo);
	} catch (error) {
		console.error('Error en createMovimientoHabitacion:', error);
		res.status(500).json({ message: 'Error al crear movimiento habitaci√≥n' });
	}
};


// üî∏ Actualizar movimiento habitaci√≥n (API)
export const updateMovimientoHabitacion = async (req, res) => {
	try {
		const { id } = req.params;
		const movimiento = await MovimientoHabitacion.findByPk(id);
		if (!movimiento)
			return res.status(404).json({ message: 'Movimiento no encontrado' });

		const {
			id_admision,
			id_habitacion,
			id_cama, 
			fecha_hora_ingreso,
			fecha_hora_egreso,
			id_mov,
			estado,
		} = req.body;
		await movimiento.update({
			id_admision,
			id_habitacion,
			id_cama, 
			fecha_hora_ingreso,
			fecha_hora_egreso: fecha_hora_egreso || null,
			id_mov,
			estado,
		});
		res.json(movimiento);
	} catch (error) {
		res
			.status(500)
			.json({ message: 'Error al actualizar movimiento habitaci√≥n' });
	}
};

// üî∏ Eliminar movimiento habitaci√≥n (API)
export const deleteMovimientoHabitacion = async (req, res) => {
	try {
		const movimiento = await MovimientoHabitacion.findByPk(req.params.id);
		if (!movimiento)
			return res.status(404).json({ message: 'Movimiento no encontrado' });

		await movimiento.destroy();
		res.sendStatus(204);
	} catch (error) {
		res
			.status(500)
			.json({ message: 'Error al eliminar movimiento habitaci√≥n' });
	}
};

